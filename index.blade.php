        function promo() {
            var code = document.querySelector('input[name="promo"]').value;
            if (code == '') {
                return false;
            }
            $.ajax({
                url: "{{ url('submit-bonus-amount') }}",
                type: 'POST',
                headers: {
                    'X-CSRF-TOKEN': "{{ csrf_token() }}",
                },
                data: {
                    code: code,
                },
                dataType: 'json',
                success: function(res) {
                    closePromo();
                    document.querySelector('input[name="promo"]').value = '';
                    message(res.message);
                }
            });
        }
    </script>

